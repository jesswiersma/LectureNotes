/*
Variables: named containers for storing data values.
- We name the variables so that we can refer to them and the values they hold at a later time.
*/

//Best practice is to use "let" and "const" to declare variables

let a = 2;

/*
(1)   (2)  (3)   (4)
let    b     =      2

1. JavaScript keyword to denote the creation of a variable
2. the name of the variable in which the developer refers to in order to grab the value stored.
3. Assignment operator
4. The initial value
*/

//The declaration is to the left of the assignment operator (=)
//This is where the variable is created by utilizing a designated JavaScript keyword(var, let, const) and giving it a name.

let s; //This is a variable declaration

//The initilaization is to the right side of the assignment operator (=)
//This is where the developer sets the value for the declared variable
//     (1)
let x = 10;

//1. initialization

let y;
console.log("Declaration:" , y);

y = 10;
console.log("initialization 1 :",y);

y = 35;
console.log("initialization 2:" , y);

//with const, the value will stay the same no matter what

